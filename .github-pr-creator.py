    headers = {
        'Authorization': f'token {github_token}',
        'Accept': 'application/vnd.github.v3+json',
        'Content-Type': 'application/json'
    }
    
    pr_data = {
        'title': pr_title,
        'head': head_branch,
        'base': base_branch,
        'body': pr_body,
        'maintainer_can_modify': True
    }
    
    try:
        print("🚀 Sending PR creation request...")
        response = requests.post(api_url, headers=headers, json=pr_data)
        
        if response.status_code == 201:
            pr_info = response.json()
            pr_number = pr_info['number']
            pr_url = pr_info['html_url']
            
            print(f"✅ PR created successfully!")
            print(f"📋 PR #{pr_number}: {pr_title}")
            print(f"🔗 URL: {pr_url}")
            
            # Save PR info for later reference
            pr_info_file = '.github-pr-info.json'
            with open(pr_info_file, 'w') as f:
                json.dump({
                    'pr_number': pr_number,
                    'pr_url': pr_url,
                    'created_at': datetime.now().isoformat(),
                    'title': pr_title,
                    'branch': head_branch
                }, f, indent=2)
            
            print(f"💾 PR info saved to {pr_info_file}")
            return True
            
        else:
            print(f"❌ PR creation failed: {response.status_code}")
            print(f"Response: {response.text}")
            return False
            
    except Exception as e:
        print(f"❌ Error creating PR: {e}")
        return False

